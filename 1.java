public class Main{  //Mainがクラス名　クラス名とファイル名を同じにするルールがある
  public static void main(String[] args){ //外側のブロックでクラスブロックと言う
    System.out.println("aa"); //内側のブロックでメソッドブロックという
    
    
    int myAge; //型　変数名;
    myAge = 26; //2つの単語が合わさる時２つ目の単語の頭文字を大文字にする
    System.out.println(myAge);

    int age = 19; //まとめて書くことも可能
    System.out.println(age);
  }
}
//メソッドブロックを記述して命令する
